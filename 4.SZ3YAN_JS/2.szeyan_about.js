function turn1() {
    document.querySelector(".moreaboutMe__turn1").classList.toggle("turning");
    document.querySelector(".content1").classList.toggle("blue");

    if (document.querySelector(".content1").classList.contains("blue")) {
        document.querySelector("#overlay").style.opacity = "1";
    }

    else {
        document.querySelector("#overlay").style.opacity = "0";
    }
}

